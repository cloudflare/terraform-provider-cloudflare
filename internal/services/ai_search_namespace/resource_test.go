// testClient returns a Cloudflare API client supporting either API key+email or
// API token credentials. Falls back to acctest.SharedClient() when only key+email
// are set, but constructs a token-based client when CLOUDFLARE_API_TOKEN is set.
func testClient() *cloudflare.Client {
	if token := os.Getenv("CLOUDFLARE_API_TOKEN"); token != "" {
		return cloudflare.NewClient(option.WithAPIToken(token))
	}
	return acctest.SharedClient()
}

